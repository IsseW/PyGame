def placeObject(world, obj, x, y):
    widthWorld = len(world)
    heightWorld = len(world[0])
    height = len(obj)
    width = len(obj[0])
    if x + width > widthWorld or y + height > heightWorld:
        return

    for _x in range(width):
        for _y in range(height):
            world[x + _x][y + _y] = obj[_y][_x]
